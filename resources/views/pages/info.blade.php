<h1>Over AAS 2.0</h1>

<hr/>

<p>AAS 2.0 is ontwikkeld door <a href="mailto:robin@anderwijs.nl">Robin Klaassen</a> in april-mei 2015. Het systeem draait op het PHP framework <a href="http://laravel.com/">Laravel</a> en de voorkant is gebaseerd op <a href="http://getbootstrap.com/">Bootstrap</a>, met de <a href="https://bootswatch.com/cosmo/">Cosmo</a> template. Interactieve tabellen worden mogelijk gemaakt door <a href="https://datatables.net">DataTables</a>, kaarten en grafieken door Google's <a href="https://developers.google.com/maps/">Maps</a> en <a href="https://developers.google.com/chart/">Charts</a>.</p>

<p>AAS staat voor Anderwijs Administratie Systeem en is voor het eerst digitaal ontwikkeld door Dave Liefbroer in 2008.</p>

<p>Bug reports, feature requests en/of vragen graag per mail naar <a href="mailto:webmaster@anderwijs.nl">webmaster@anderwijs.nl</a>. Wees hierbij zo specifiek mogelijk.</p>

